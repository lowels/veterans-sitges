/* ============================================================
   Utilitat per accedir a claus niades (ex: "hero.title")
   ============================================================ */
function getNestedValue(obj, keyPath) {
  return keyPath.split('.').reduce((acc, key) => {
    return acc && acc[key] !== undefined ? acc[key] : '';
  }, obj);
}

/* ============================================================
   Aplica les traduccions al DOM
   ============================================================ */
function setLanguage(lang) {
  const supported = ['ca', 'es', 'en', 'fr'];
  const safeLang = supported.includes(lang) ? lang : 'ca';

  document.documentElement.lang = safeLang;
  localStorage.setItem('lang', safeLang);

  // Text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const text = getNestedValue(translations[safeLang], key);
    if (text) el.textContent = text;
  });

  // Placeholder attributes
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    const text = getNestedValue(translations[safeLang], key);
    if (text) el.placeholder = text;
  });

  // aria-label attributes
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const key = el.dataset.i18nAria;
    const text = getNestedValue(translations[safeLang], key);
    if (text) el.setAttribute('aria-label', text);
  });

  // Actualitza els botons del selector d'idioma
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('lang-btn--active', btn.dataset.lang === safeLang);
  });

  // Emmagatzema globalment
  window.currentLang = safeLang;
}

/* ============================================================
   Inicialització: detecta idioma del navegador o localStorage
   ============================================================ */
(function initI18n() {
  const saved = localStorage.getItem('lang');
  const browserLang = (navigator.language || navigator.userLanguage || 'ca').split('-')[0];
  const supported = ['ca', 'es', 'en', 'fr'];

  let lang = 'ca';
  if (saved && supported.includes(saved)) {
    lang = saved;
  } else if (supported.includes(browserLang)) {
    lang = browserLang;
  }

  // Executa quan el DOM estigui llest
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => setLanguage(lang));
  } else {
    setLanguage(lang);
  }
})();
